# include <stdio.h>
# include <stdlib.h>

# include "Status.h"

/*----------------------------------------------------------------------------------------------------------------------------------
! . Definitions.
!---------------------------------------------------------------------------------------------------------------------------------*/
# define STATUSDEBUG

/*----------------------------------------------------------------------------------------------------------------------------------
! . Size of the enumeration.
!---------------------------------------------------------------------------------------------------------------------------------*/
CSize Status_CSize ( void )
{
    Status self = Status_Continue ;
    return sizeof ( self ) ;
}

/*----------------------------------------------------------------------------------------------------------------------------------
! . Safe setting - no checking.
!---------------------------------------------------------------------------------------------------------------------------------*/
void Status_SafeSet ( Status *self, Status status ) { if ( self != NULL ) (*self) = status ; }

/*----------------------------------------------------------------------------------------------------------------------------------
! . Setting.
!---------------------------------------------------------------------------------------------------------------------------------*/
void Status_Set ( Status *self, Status status )
{
# ifdef STATUSDEBUG
if ( ( status != Status_Continue ) && ( status != Status_Null ) && ( status != Status_Success ) ) printf ( "\nSTATUS DEBUG> Status Flag = %d", status ) ;
# endif
    if ( self != NULL ) (*self) = status ;
}
