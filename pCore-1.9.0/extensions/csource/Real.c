/*----------------------------------------------------------------------------------------------------------------------------------
! . Safe minimum.
!---------------------------------------------------------------------------------------------------------------------------------*/
/* . This is the smallest positive machine number such that its inverse does not overflow. */
Real Real_SafeMinimum ( void ) { return dlamch_ ( "S" ) ; }

/*----------------------------------------------------------------------------------------------------------------------------------
! . Unit roundoff.
!---------------------------------------------------------------------------------------------------------------------------------*/
/* . This is the smallest positive machine number u such that 1.0 + u != 1.0. */
Real Real_UnitRoundOff ( void )
{
    Real c, u ;
    u = 1.0e+00 ;
    do
    {
        u *= 0.5e+00 ;
        c  = 1.0e+00 + u ;
    }
    while ( c != 1.0e+00 ) ;
    return ( 2.0e+00 * u ) ;
}
